messages = [
    "Hello there!",
    "How's it going?",
    "Nice weather today",
    "Running a bit late",
    "Thanks for waiting",
    "See you soon",
    "Take care",
    "Good luck!",
    "Almost done",
    "On my way",
]


def show_messages(message_list):
    """
    This function print each message inside a message list
    """
    for message in message_list:
        print(message)

show_messages(messages)