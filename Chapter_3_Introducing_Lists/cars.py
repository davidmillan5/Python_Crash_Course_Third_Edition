cars = ['bmw', 'audi', 'subaru', 'toyota']
cars.sort()
print(cars)

## Sort In Reverse

cars.sort(reverse=True)
print(cars)

## Sorting a List Temporaly

cars = ['bmw', 'audi', 'subaru', 'toyota']
print("Here is the Original list:")
print(cars)

print("\nHere is the sorted list:")
print(sorted(cars))

print("\nHere is the original list again:")
print(cars)


## Reverse Order

cars.reverse()
print(cars)

## Finding The Lenght Of a list

print(len(cars))